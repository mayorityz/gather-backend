import mongoose from 'mongoose';

const productSchema = mongoose.Schema({
    artisan: {
        type: String,
        required: true
    },
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    likes: {
        type: [String],
        default: []
    },
    comments: {
        type: [String],
        default: []
    },
    createdAt: {
        type: Date,
        default: new Date()
    }
});

const Product = mongoose.model('product', productSchema);

export default Product;
